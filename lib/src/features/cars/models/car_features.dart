  // الحصول على جميع المميزات في قائمة واحدة
  static List<String> get allFeatures {
    final List<String> features = [];
    categories.forEach((_, value) => features.addAll(value));
    return features;
  }

  // الحصول على الفئة التي تنتمي إليها الميزة
  static String? getCategoryForFeature(String feature) {
    for (var entry in categories.entries) {
      if (entry.value.contains(feature)) {
        return entry.key;
      }
    }
    return null;
  }
} 